    private void Listar() {
        model = (DefaultTableModel) tblDatos.getModel();
        if (model.getRowCount() > 0) {
            model.setNumRows(0);
        }
        Object[] obj = new Object[7];
        try {
            lista = vjc.findVendedorEntities();
            for (int i = 0; i < lista.size(); i++) {
                vendedor = (Vendedor) lista.get(i);
                obj[0] = vendedor.getIdVendedor();
                obj[1] = vendedor.getDni();
                obj[2] = vendedor.getNombres();
                obj[3] = vendedor.getTelefono();
                obj[4] = vendedor.getEstado().equals("1") ? "Activo" : "Inactivo";
                obj[5] = vendedor.getUser();
                obj[6] = vendedor.getEmail();
                model.addRow(obj);
            }
            tblDatos.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }
    
