    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Vendedor vendedor = new Vendedor();
            vendedor.setDni(txtDni.getText());
            vendedor.setNombres(txtNombres.getText());
            vendedor.setTelefono(txtTelefono.getText());
            vendedor.setEstado(chkEstado.isSelected() ? "1" : "0");
            vendedor.setUser(txtUser.getText());
            vendedor.setEmail(txtEmail.getText());
            int rsp = 0;
            switch (accion) {
                case "Agregar":
                    vjc.create(vendedor);
                    rsp = 1;
                    break;
                case "Editar":
                    vendedor.setIdVendedor(Integer.parseInt(txtId.getText()));
                    vendedor.setVentasList(vjc.findVendedor(Integer.parseInt(txtId.getText())).getVentasList());
                    vjc.edit(vendedor);
                    rsp = 1;
                    break;
                default:
                    JOptionPane.showConfirmDialog(null, "No se encontró la opcion", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
            if (rsp == 1) {
                JOptionPane.showMessageDialog(null, "Operacion realizada correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                Listar();
                disableForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblDatos.getSelectedRowCount() == 1) {
            try {
                this.accion = "Editar";
                enableForm();
                int row = tblDatos.getSelectedRow();
                txtId.setText(tblDatos.getValueAt(row, 0).toString());
                Vendedor vendedor = new Vendedor();
                vendedor = vjc.findVendedor(Integer.parseInt(txtId.getText()));
                txtDni.setText(vendedor.getDni());
                txtNombres.setText(vendedor.getNombres());
                txtTelefono.setText(vendedor.getTelefono());
                chkEstado.setSelected(vendedor.getEstado().equals("1"));
                txtUser.setText(vendedor.getUser());
                txtEmail.setText(vendedor.getEmail());
                if (chkEstado.isSelected()) {
                    chkEstado.setText("Activo");
                } else {
                    chkEstado.setText("Inactivo");
                }
                txtNombres.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tiene que seleccionar un registro", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

