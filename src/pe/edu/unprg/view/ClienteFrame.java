    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Cliente cliente = new Cliente();
            cliente.setNombres(txtNombres.getText());
            cliente.setDni(txtDni.getText());
            cliente.setDireccion(txtDireccion.getText());
            cliente.setEstado(chkEstado.isSelected() ? "1" : "0");
            cliente.setTelefono(txtTelefono.getText());
            cliente.setEmail(txtEmail.getText());
            int rsp = 0;
            switch (accion) {
                case "Agregar":
                    cjc.create(cliente);
                    rsp = 1;
                    break;
                case "Editar":
                    cliente.setIdCliente(Integer.parseInt(txtId.getText()));
                    cliente.setVentasList(cjc.findCliente(Integer.parseInt(txtId.getText())).getVentasList());
                    cjc.edit(cliente);
                    rsp = 1;
                    break;
                default:
                    JOptionPane.showConfirmDialog(null, "No se encontró la opcion", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
            if (rsp == 1) {
                JOptionPane.showMessageDialog(null, "Operacion realizada correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                Listar();
                disableForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblDatos.getSelectedRowCount() == 1) {
            try {
                this.accion = "Editar";
                enableForm();
                int row = tblDatos.getSelectedRow();
                txtId.setText(tblDatos.getValueAt(row, 0).toString());
                Cliente cliente = new Cliente();
                cliente = cjc.findCliente(Integer.parseInt(txtId.getText()));
                txtDni.setText(cliente.getDni());
                txtNombres.setText(cliente.getNombres());
                txtDireccion.setText(cliente.getDireccion());
                chkEstado.setSelected(cliente.getEstado().equals("1"));
                txtTelefono.setText(cliente.getTelefono());
                txtEmail.setText(cliente.getEmail());
                if (chkEstado.isSelected()) {
                    chkEstado.setText("Activo");
                } else {
                    chkEstado.setText("Inactivo");
                }
                txtNombres.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tiene que seleccionar un registro", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

