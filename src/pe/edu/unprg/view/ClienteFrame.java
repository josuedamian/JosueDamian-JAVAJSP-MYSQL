    private void Listar() {
        model = (DefaultTableModel) tblDatos.getModel();
        if (model.getRowCount() > 0) {
            model.setNumRows(0);
        }
        Object[] obj = new Object[7];
        try {
            lista = cjc.findClienteEntities();
            for (int i = 0; i < lista.size(); i++) {
                cliente = (Cliente) lista.get(i);
                obj[0] = cliente.getIdCliente();
                obj[1] = cliente.getDni();
                obj[2] = cliente.getNombres();
                obj[3] = cliente.getDireccion();
                obj[4] = cliente.getEstado().equals("1") ? "Activo" : "Inactivo";
                obj[5] = cliente.getTelefono();
                obj[6] = cliente.getEmail();
                model.addRow(obj);
            }
            tblDatos.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }

